SELECT DISTINCT
	TEC.[Control], TEC.[EquipmentClassID],
	TEC.[Description], TEC.ClassType,
	ISNULL(CAST(TEC.OverheadAmount AS VARCHAR), ''), TEC.Meter1Type,
	TEC.Meter2Type [Meter 2 Type], ISNULL(CAST(TEC.Meter1EditRange AS VARCHAR), ''),
	ISNULL(CAST(TEC.Meter2EditRange AS VARCHAR), ''),
	ISNULL(CAST(TEC.Meter1MaxValue AS VARCHAR), ''),
	ISNULL(CAST(TEC.Meter2MaxValue AS VARCHAR), ''),
	ISNULL(CAST(TEC.ComebackRange AS VARCHAR), ''),
	ISNULL(CAST(TEC.PMSoonDue AS VARCHAR), ''), ISNULL(CAST(TEC.PMFuelQuantityOverride AS VARCHAR), ''),
	ISNULL(CAST(TEC.PMServicesInOneCycle AS VARCHAR), ''), ISNULL(CAST(TEC.PMQuantityOfTimeUnits AS VARCHAR), ''),
	TEC.PMTimeUnit,
	TEC.MeterSoonDue, TEC.ClassPMCycles,
	TEC.PMDetails, TEC.IndividualPM,
	TEC.[ValueDepreciationID],
	TEC.MeterTypes, TEC.Maintenance,
	TEC.[PMProgram], TEC.Standards,
	TEC.RentalRates, TEC.Resources
